#!/usr/bin/env node
/**
 * Prepare a standalone, git-installable package.
 *
 * The monorepo CLI cannot be installed via `npm install -g github:org/repo`
 * directly because its tsconfig extends a workspace path and `dist/` is
 * gitignored. This script assembles a self-contained, PRE-BUILT package under
 * `standalone/` that can be pushed to a dedicated distribution repo
 * (e.g. derivoai/derivocli) and installed straight from GitHub or as a .tgz.
 *
 * Output `standalone/` contains:
 *   bin/  dist/  README.md  LICENSE  package.json (no monorepo coupling)
 *
 * Usage: node scripts/prepare-standalone.mjs
 */
import { execSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const pkgRoot = path.resolve(here, '..');
const outDir = path.join(pkgRoot, 'standalone');

function log(msg) {
  console.log(`  ${msg}`);
}

// 1. Build to ensure dist/ is current.
log('Building (tsc)...');
execSync('npm run build', { cwd: pkgRoot, stdio: 'inherit' });

// 2. Reset the output directory.
fs.rmSync(outDir, { recursive: true, force: true });
fs.mkdirSync(outDir, { recursive: true });

// 3. Copy production assets.
for (const entry of ['bin', 'dist', 'README.md', 'LICENSE']) {
  const from = path.join(pkgRoot, entry);
  const to = path.join(outDir, entry);
  if (!fs.existsSync(from)) continue;
  fs.cpSync(from, to, { recursive: true });
  log(`Copied ${entry}`);
}

// 4. Write a self-contained package.json (no workspace coupling, dist committed).
const pkg = JSON.parse(fs.readFileSync(path.join(pkgRoot, 'package.json'), 'utf8'));
const standalonePkg = {
  name: pkg.name,
  version: pkg.version,
  type: pkg.type,
  description: pkg.description,
  keywords: pkg.keywords,
  license: pkg.license,
  author: pkg.author,
  homepage: pkg.homepage,
  repository: pkg.repository,
  bugs: pkg.bugs,
  bin: pkg.bin,
  exports: pkg.exports,
  engines: pkg.engines,
  // No build/lint scripts: dist is committed, so installs need no toolchain.
  dependencies: pkg.dependencies,
  publishConfig: pkg.publishConfig,
};
fs.writeFileSync(
  path.join(outDir, 'package.json'),
  JSON.stringify(standalonePkg, null, 2) + '\n',
);
log('Wrote standalone package.json');

// 5. Drop an instructions file for the distribution repo.
const instructions = `# Derivo (distribution build)

This directory is a PRE-BUILT, standalone copy of the Derivo CLI generated by
\`scripts/prepare-standalone.mjs\`. It includes a committed \`dist/\` so it can be
installed without a build step.

Install from this repo:

    npm install -g github:derivoai/derivocli

Install the local .tgz:

    npm pack
    npm install -g ./derivo-${pkg.version}.tgz

Do not edit files here by hand — regenerate with \`pnpm pack:standalone\` in the
monorepo.
`;
fs.writeFileSync(path.join(outDir, 'DISTRIBUTION.md'), instructions);

console.log(`\nStandalone package ready at: ${outDir}`);
console.log('Push its contents to derivoai/derivocli to enable GitHub installs.\n');
